
public class Literal {

	public static void main(String[] args) {
		int n = 100;
		System.out.println(n);
		n = 0144;
		System.out.println(n);
		n = 0x64;
		System.out.println(n);
		
		double d = 3.14159;
		System.out.println(d);
		d = 3.14e5;
		System.out.println(d);
		
		String s = "Hello";
		System.out.println(s);
		
		char c = 'A';
		System.out.println(c);
		c = 66;
		System.out.println(c);
		c = 43;
		System.out.println(c);
		
		c = '\u0041';
		System.out.println(c);
		
		
	}

}
