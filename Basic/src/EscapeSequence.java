
public class EscapeSequence {

	public static void main(String[] args) {
        System.out.println("Hello world!!!");
        System.out.print("Hello world!!!");
        System.out.print("Hello world!!!\n");
        System.out.println("Hello world!!!");
        
        System.out.println("Hello\tworld!!!");
        System.out.println("\"Hello world!!!\"");

	}

}
